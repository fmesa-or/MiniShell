#include "minishell.h"

/***********************************
*zero = void str; 1 = there's chars*
***********************************/
int	str_isspace(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!ft_isspace(str[i]))
			return (1);
		i++;
	}
	return (0);
}
